export interface ResolvedPlace extends Place { name: string; }

// Split location strings into mapped points and unmapped leftovers (variants that
// resolve to the same coordinate are collapsed).
export function resolvePlaces(locations: string[] | undefined): { mapped: ResolvedPlace[]; unmapped: string[] } {
  const mapped: ResolvedPlace[] = [];
  const unmapped: string[] = [];
  const seen = new Set<string>();
  for (const name of locations ?? []) {
    const p = PLACES[name];
    if (!p) { unmapped.push(name); continue; }
    const key = p.lat + "," + p.lon;
    if (seen.has(key)) continue;
    seen.add(key);
    mapped.push({ name, lat: p.lat, lon: p.lon, kind: p.kind });
  }
  return { mapped, unmapped };
}
